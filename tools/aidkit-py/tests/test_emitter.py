class TestRoundTrip:
    def test_parse_emit_parse(self):
        """Parse -> emit -> parse should produce equivalent data."""
        original = """\
@module mypackage
@lang go
@version 1.0.0
@purpose A test module

---

@fn DoSomething
@sig func DoSomething(ctx context.Context) error
@purpose Does something useful
@errors
  ErrNotFound — item not found [src: handler.go:42]

---

@type Config
@kind struct
@purpose Configuration
@fields
  Name string — config name
"""
        aid1, _ = parse_string(original)
        emitted = emit(aid1)
        aid2, _ = parse_string(emitted)

        assert aid2.header.module == aid1.header.module
        assert aid2.header.lang == aid1.header.lang
        assert len(aid2.entries) == len(aid1.entries)
        assert aid2.entries[0].name == aid1.entries[0].name
        assert aid2.entries[1].name == aid1.entries[1].name
