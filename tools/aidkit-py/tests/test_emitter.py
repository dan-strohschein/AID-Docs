"""Tests for the AID emitter."""
from __future__ import annotations

from aidkit.emitter import emit
from aidkit.model import AidFile, Annotation, Entry, Field, Header, Workflow
from aidkit.parser import parse_string


class TestEmitHeader:
    def test_basic_header(self):
        aid = AidFile(header=Header(module="mymod", lang="go", version="1.0", purpose="Test"))
        text = emit(aid)
        assert "@module mymod\n" in text
        assert "@lang go\n" in text
        assert "@version 1.0\n" in text
        assert "@purpose Test\n" in text

    def test_manifest_header(self):
        aid = AidFile(header=Header(), is_manifest=True)
        text = emit(aid)
        assert text.startswith("@manifest\n")

    def test_deps(self):
        aid = AidFile(header=Header(module="t", deps=["fmt", "os"]))
        text = emit(aid)
        assert "@deps [fmt, os]\n" in text

    def test_depends(self):
        aid = AidFile(header=Header(module="t", depends=["pkg/a", "pkg/b"]))
        text = emit(aid)
        assert "@depends [pkg/a, pkg/b]\n" in text

    def test_comments(self):
        aid = AidFile(header=Header(module="t"), comments=["// generated by tool"])
        text = emit(aid)
        assert text.startswith("// generated by tool\n")


class TestEmitEntries:
    def test_fn_entry(self):
        entry = Entry(kind="fn", name="DoThing", fields={
            "purpose": Field(name="purpose", inline_value="Does a thing"),
            "sig": Field(name="sig", inline_value="func DoThing()"),
        })
        aid = AidFile(header=Header(module="t"), entries=[entry])
        text = emit(aid)
        assert "@fn DoThing\n" in text
        assert "@purpose Does a thing\n" in text
        assert "@sig func DoThing()\n" in text

    def test_multiline_field(self):
        entry = Entry(kind="fn", name="Foo", fields={
            "errors": Field(name="errors", lines=["ErrNotFound — item missing", "ErrTimeout — deadline exceeded"]),
        })
        aid = AidFile(header=Header(module="t"), entries=[entry])
        text = emit(aid)
        assert "@errors\n" in text
        assert "  ErrNotFound" in text
        assert "  ErrTimeout" in text

    def test_multiple_entries_separated(self):
        e1 = Entry(kind="fn", name="A", fields={"sig": Field(name="sig", inline_value="func A()")})
        e2 = Entry(kind="fn", name="B", fields={"sig": Field(name="sig", inline_value="func B()")})
        aid = AidFile(header=Header(module="t"), entries=[e1, e2])
        text = emit(aid)
        assert text.count("---") == 2


